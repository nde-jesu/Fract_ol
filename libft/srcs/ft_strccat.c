char	*ft_strccat(char *dst, const char *src, char c)
{
	int		i;
	int		j;

	i = 0;
	j = 0;
	while (dst[i])
		++i;
	while (src[j] && src[j] != c)
	{
		dst[i] = src[j];
		++i;
		++j;
	}
	dst[i] = '\0';
	return (dst);
}
